class Datatypes
{
	public static void main(String p[])
	{
		int i = 90;
		System.out.println("Interger:" + i);
		char c = 'A';
		System.out.println("Char:" + c);
		float f = 2.1f;
		System.out.println("Float:" + f);
		byte b = 4;
		System.out.println("Byte:" + b);
		double d = 4.65654564;
		System.out.println("Double:" + d);
	}
}